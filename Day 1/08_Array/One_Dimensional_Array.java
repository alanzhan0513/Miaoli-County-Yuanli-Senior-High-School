public class One_Dimensional_Array
{
    public static void main(String[] args)
    {
        int[] ages = new int[5];

        ages[0] = 18;
        ages[1] = 12;
        ages[2] = 15;
        ages[3] = 55;
        ages[4] = 25;

        for (int i = 0; i < 5; i++)
        {
            System.out.println(ages[i]);
        }
    }
}