public class Arithmetic
{
    public static void main(String[] args)
    {
        // 宣告變數與值
        int x = 10;
        int y = 5;
 
        System.out.println("x = " + x);
        System.out.println("y = " + y);
 
        // 加法示範
        System.out.println("x + y = " + (x + y));
 
        // 減法示範
        System.out.println("x - y = " + (x - y));
 
        // 乘法示範
        System.out.println("x * y = " + (x * y));
 
        // 除法示範
        System.out.println("x / y = " + (x / y));
 
        // 餘數運算
        System.out.println("x % y = " + (x % y));
    }
}